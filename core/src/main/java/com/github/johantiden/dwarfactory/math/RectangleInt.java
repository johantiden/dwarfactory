package com.github.johantiden.dwarfactory.math;

public class RectangleInt {
    public int x;
    public int y;
    public int width;
    public int height;

    public RectangleInt(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
